     private DefaultTableModel montarTabela(){
        RelatorioDAO dao = new RelatorioDAO();
        String[] colunas = {"ID", "Data", "Funcionario", "Matricula", "Tipo", "Patrimônio", "Marca", "Relatório"};
        
        //Criamos a tabela
        DefaultTableModel tabela = new DefaultTableModel(colunas,0);
        
        List<Relatorio> lista = RelatorioDAO.listarTodosRelatorio();
        
        for (int i = 0; i < lista.size(); i++) {
            Relatorio relatorio = lista.get(i);
            
            String[] linha = {
                Integer.toString(relatorio.getId()),
                relatorio.getData(),
                relatorio.getNome(),
                Integer.toString(relatorio.getMatricula()),
                relatorio.getMaquina(),
                Integer.toString(relatorio.getPatrimonio()),
                relatorio.getMarca(),
                relatorio.getRelatorio(),
            };
            tabela.addRow(linha);
        }
        return tabela;
    }


  
    
