    private void BotaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoExcluirActionPerformed
        
    int linhaSelecionada = jTable1.getSelectedRow();

    if (linhaSelecionada != -1) {
        String valor = jTable1.getValueAt(linhaSelecionada, 0).toString();

        try {
            int idConsulta = Integer.parseInt(valor);

            int confirmacao = JOptionPane.showConfirmDialog(
                null,
                "Tem certeza que deseja excluir este relatório?",
                "Confirmação",
                JOptionPane.YES_NO_OPTION
            );

            if (confirmacao == JOptionPane.YES_OPTION) {
                RelatorioDAO dao = new RelatorioDAO();
                dao.excluirConsulta(idConsulta);

                // Remove a linha da tabela
                ((DefaultTableModel) jTable1.getModel()).removeRow(linhaSelecionada);

                JOptionPane.showMessageDialog(null, "Relatório excluído com sucesso.");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "ID do relatório inválido.");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao excluir relatório: " + ex.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(null, "Selecione uma relatório para excluir.");
    }

    }//GEN-LAST:event_BotaoExcluirActionPerformed

    private void BotaoBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoBuscarActionPerformed
                                               
    String texto = txtPatrimonio.getText().trim();
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // limpa a tabela

    if (texto.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Digite um patrimônio para buscar.");
        return;
    }

    try {
        int patrimonio = Integer.parseInt(texto);
        RelatorioDAO dao = new RelatorioDAO();
        List<Maquinas> maquinas = dao.buscarPorPatrimonio(patrimonio);

        if (maquinas.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nenhuma máquina encontrada com este patrimônio.");
        } else {
            for (Maquinas m : maquinas) {
                model.addRow(new Object[]{
                    m.getId(),
                    m.getPatrimonio(),
                    m.getTipo(),
                    m.getMarca(),
                    m.getFuncionario(),
                    m.getMatricula(),
                    m.getData()
                });
            }
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Patrimônio inválido. Digite somente números.");
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Erro ao buscar máquinas: " + e.getMessage());
    }
    }//GEN-LAST:event_BotaoBuscarActionPerformed

