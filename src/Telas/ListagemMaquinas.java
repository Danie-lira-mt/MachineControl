private void excluirMaquinaSelecionada() {
    int linhaSelecionada = jTable2.getSelectedRow();

    if (linhaSelecionada != -1) {
        String valor = jTable2.getValueAt(linhaSelecionada, 0).toString();

        try {
            int idMaquina = Integer.parseInt(valor);

            int confirmacao = JOptionPane.showConfirmDialog(
                null,
                "Tem certeza que deseja excluir esta máquina?",
                "Confirmação",
                JOptionPane.YES_NO_OPTION
            );

            if (confirmacao == JOptionPane.YES_OPTION) {
                MaquinasDAO dao = new MaquinasDAO();
                dao.excluirMaquina(idMaquina);

                // Remove a linha da tabela
                ((DefaultTableModel) jTable2.getModel()).removeRow(linhaSelecionada);

                JOptionPane.showMessageDialog(null, "Máquina excluída com sucesso.");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "ID da Máquina inválido.");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao excluir máquina: " + ex.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(null, "Selecione uma máquina para excluir.");
    }

    }//GEN-LAST:event_BotaoExcluirActionPerformed

