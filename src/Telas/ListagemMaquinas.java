  private DefaultTableModel montarTabelaMaquinas() {
    String[] colunas = {"Patrimônio", "Tipo", "Marca", "Funcionário", "Matrícula", "Data"};
    DefaultTableModel tabelaModelo = new DefaultTableModel(colunas, 0);

    // Chama o método estático listarTodos() da classe MaquinasDAO para obter os dados
    List<Maquinas> listaCompleta = MaquinasDAO.listarTodos();

    for (Maquinas m : listaCompleta) {
        String[] linha = {
            Integer.toString(m.getPatrimonio()),
            m.getTipo(),
            m.getMarca(),
            m.getFuncionario(),
            Integer.toString(m.getMatricula()),
            m.getData()
        };
        tabelaModelo.addRow(linha);
    }

    return tabelaModelo;
}

    

